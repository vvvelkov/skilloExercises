package Lecture_01;

public class TriangleArea_03 {
    public static void main(String[] args) {

        triangleArea();
    }

    private static void triangleArea() {

        float b = 5.3F;
        float h = 4.2F;

        float area = (b * h) / 2.0F;

        System.out.println("Triangle Area is: " + area);

    }
}